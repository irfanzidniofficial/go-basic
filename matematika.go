package main

import "fmt"

func main()  {
	var a = 10
	var b = 10

	var c = a +b
	fmt.Println(c)

	
	fmt.Println("--------------------------------")
	// Augmented Assignments

	var i = 10
	i+=15 // i = i+15
	fmt.Println(i)

	// Unary Opeator
	println("------Unary Opeator------")

	var j = 1
	j++
	fmt.Println(j)
	j++
	fmt.Println(j)
	
}