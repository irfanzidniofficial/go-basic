package main

import "fmt"

func main()  {
	var name string
	name = "Irfan Zidni"
	fmt.Println(name)

	name = "Muhammad Irfan Zidni"
	fmt.Println(name)

	// inisialisasi langsung

	var firstName = "Irfan"
	fmt.Println(firstName)


	// membuat variable

	lastName := "Zidn"
	fmt.Println(lastName)

	lastName = "Zidni"
	fmt.Println(lastName)

	// Membuat multiple variable
	
	var (
        nickName = "Irfan"
        middleName = "Zidni"
    )
    fmt.Println(nickName)
    fmt.Println(middleName)

	
}